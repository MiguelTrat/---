 jQuery(document).ready(function(){
  $('.header__link-one').mousemove(function(e){
    var rXP = (e.pageX - this.offsetLeft-$(this).width()/2);
    var rYP = (e.pageY - this.offsetTop-$(this).height()/2);
    $('.header__link-one').css('text-shadow', +rYP/10+'px '+rXP/80+'px #a0f016, '+rYP/8+'px '+rXP/60+'px  #527514, '+rXP/70+'px '+rYP/12+'px #253803');
  });
});

